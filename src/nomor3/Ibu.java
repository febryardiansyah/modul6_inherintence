package nomor3;

public class Ibu {
    int idIbu;
    String namaIbu;
    int umurIbu;

    public Ibu(int idIbu, String namaIbu, int umurIbu) {
        this.idIbu = idIbu;
        this.namaIbu = namaIbu;
        this.umurIbu = umurIbu;
    }
    public int getIdIbu() {
        return idIbu;
    }
    public String getNamaIbu() {
        return namaIbu;
    }
    public int getUmurIbu() {
        return umurIbu;
    }
}
